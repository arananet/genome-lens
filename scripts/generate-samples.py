CHROM_ORDER = [str(i) for i in range(1, 23)] + ["X", "Y"]
CHROM_RANK  = {c: i for i, c in enumerate(CHROM_ORDER)}
CHROMS      = list(CHROM_LENGTHS.keys())
WEIGHTS     = [CHROM_LENGTHS[c] for c in CHROMS]
ALLELE_PAIRS = [("A","G"), ("C","T"), ("A","C"), ("G","T"), ("A","T"), ("C","G")]

# ── Knowledge-base rsids — present in every sample file ──────────────────────
