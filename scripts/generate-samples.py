def random_genotype(rng: random.Random, a: str, b: str) -> tuple[str, str]:
    return rng.choices([(a, a), (a, b), (b, b)], weights=[0.30, 0.50, 0.20])[0]

def filler_snps(rng: random.Random, n: int, used: set, rsid_start: int):
    result = []
    counter = rsid_start
    for _ in range(n):
        chrom = rng.choices(CHROMS, weights=WEIGHTS)[0]
        while True:
            pos = rng.randint(10_000, CHROM_LENGTHS[chrom] - 10_000)
            if (chrom, pos) not in used:
                used.add((chrom, pos))
                break
        a, b = rng.choice(ALLELE_PAIRS)
        gt = random_genotype(rng, a, b)
        result.append((f"rs{counter}", chrom, pos, gt[0], gt[1]))
        counter += 1
    return result

def sort_snps(snps):
    return sorted(snps, key=lambda r: (CHROM_RANK.get(r[1], 99), r[2]))


