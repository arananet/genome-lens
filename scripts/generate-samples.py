# ── MyHeritage (Low-pass WGS) format ─────────────────────────────────────────

def make_myheritage(outdir: str):
    rng = random.Random(77)
    kb_rows = [
        ("rs4680",      "22", 19951271,   "AG"),
        ("rs762551",    "15", 75041917,   "AC"),
        ("rs1801133",   "1",  11856378,   "GG"),
        ("rs1801131",   "1",  11854476,   "GT"),
        ("rs6265",      "11", 27658369,   "CC"),
        ("rs1800497",   "11", 113270828,  "CT"),
        ("rs429358",    "19", 45411941,   "CT"),
        ("rs7412",      "19", 45412079,   "CC"),
        ("rs6025",      "1",  169519049,  "GG"),
        ("rs1800562",   "6",  26093141,   "GG"),
        ("rs1815739",   "11", 66560624,   "CC"),
        ("rs8192678",   "4",  23817854,   "AG"),
        ("rs9939609",   "16", 53786615,   "AA"),
        ("rs17782313",  "18", 55861362,   "TT"),
        ("rs634990",    "13", 20820493,   "CC"),
        ("rs1061170",   "1",  196659237,  "CT"),
        ("rs10490924",  "10", 124214448,  "GG"),
    ]

    used = {(r[1], r[2]) for r in kb_rows}
    filler_raw = filler_snps(rng, 2200, used, 3_000_000_000)
    filler_with_nocalls = []
    for rsid, chrom, pos, a1, a2 in filler_raw:
        gt = "--" if rng.random() < 0.02 else f"{a1}{a2}"
        filler_with_nocalls.append((rsid, chrom, pos, gt))

    kb_tuples = [(r[0], r[1], r[2], r[3]) for r in kb_rows]
    all_snps_raw = sort_snps(
        [(r[0], r[1], r[2], r[3], r[3]) for r in kb_rows]
        + [(r[0], r[1], r[2], r[3], r[3]) for r in filler_with_nocalls]
    )

    lines = [
        '##fileformat=MyHeritage',
        '##source=MyHeritage',
        '##method=Low-pass Whole Genome Sequencing',
        '##build=GRCh37',
        '##',
        '## IMPORTANT: SYNTHETIC TEST FILE — fictional person, not real data.',
        '## Generated for genome-lens functional testing. rsids rs3000000000+ are fake.',
        '##',
        '"RSID","CHROMOSOME","POSITION","RESULT"',
    ]

    # Re-sort using the proper 4-tuple list
    all_4 = sort_snps(
        [(r[0], r[1], r[2], r[3], "") for r in kb_rows]
        + [(r[0], r[1], r[2], r[3], "") for r in filler_with_nocalls]
    )
    for rsid, chrom, pos, gt, _ in all_4:
        lines.append(f'"{rsid}","{chrom}","{pos}","{gt}"')

    path = os.path.join(outdir, "synthetic-myheritage.txt")
    with open(path, "w") as f:
        f.write("\n".join(lines) + "\n")
    print(f"  MyHeritage: {path}  ({len(all_4):,} SNPs, ~2% no-calls)")


